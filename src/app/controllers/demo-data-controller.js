export async function getDemoJobDetail(jobGuid) {
  const displayedJob = { data: {} };
  await demoJobDetail(jobGuid)
    .then((response) => {
      displayedJob["data"] = response["data"];
      console.log("DDC - displayedJob: ", displayedJob["data"]);
      if (response["data"]["serviceItems"]) {
        displayedJob["data"]["siTotal"] = serviceItemsTotal(response["data"]["serviceItems"]);
      }
    })
    .catch((error) => console.error("DDC - Get DEMO job detail request failed", error));
  return displayedJob["data"];
}
